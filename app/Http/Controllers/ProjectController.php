     */
    public function store(Request $request)
    {
        /** @var  $data */
        $data=new Project();
        $data->name=$request->get('name');
        $data->type_project=$request->get('type_project');
        $data->start_date=$request->get('start_date');
        $data->notes=$request->get('notes');
        $data->save();
        return redirect('project');

    }

